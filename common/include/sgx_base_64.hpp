static const std::string base64_chars = 
             "ABCDEFGHIJKLMNOPQRSTUVWXYZ"
             "abcdefghijklmnopqrstuvwxyz"
             "0123456789+/";


static inline bool is_base64(unsigned char c) {
  return (isalnum(c) || (c == '+') || (c == '/'));
}

std::string base64_encode(unsigned char const* bytes_to_encode, unsigned int in_len) {
  std::string ret;
  int i = 0;
  int j = 0;
  unsigned char char_array_3[3];
  unsigned char char_array_4[4];

  while (in_len--) {
    char_array_3[i++] = *(bytes_to_encode++);
    if (i == 3) {
      char_array_4[0] = (char_array_3[0] & 0xfc) >> 2;
      char_array_4[1] = ((char_array_3[0] & 0x03) << 4) + ((char_array_3[1] & 0xf0) >> 4);
      char_array_4[2] = ((char_array_3[1] & 0x0f) << 2) + ((char_array_3[2] & 0xc0) >> 6);
      char_array_4[3] = char_array_3[2] & 0x3f;

      for(i = 0; (i <4) ; i++)
        ret += base64_chars[char_array_4[i]];
      i = 0;
    }
  }

  if (i)
  {
    for(j = i; j < 3; j++)
      char_array_3[j] = '\0';

    char_array_4[0] = (char_array_3[0] & 0xfc) >> 2;
    char_array_4[1] = ((char_array_3[0] & 0x03) << 4) + ((char_array_3[1] & 0xf0) >> 4);
    char_array_4[2] = ((char_array_3[1] & 0x0f) << 2) + ((char_array_3[2] & 0xc0) >> 6);
    char_array_4[3] = char_array_3[2] & 0x3f;

    for (j = 0; (j < i + 1); j++)
      ret += base64_chars[char_array_4[j]];

    while((i++ < 3))
      ret += '=';

  }

  return ret;

}
std::string base64_decode(std::string const& encoded_string) {
  int in_len = encoded_string.size();
  int i = 0;
  int j = 0;
  int in_ = 0;
  unsigned char char_array_4[4], char_array_3[3];
  std::string ret;

  while (in_len-- && ( encoded_string[in_] != '=') && is_base64(encoded_string[in_])) {
    char_array_4[i++] = encoded_string[in_]; in_++;
    if (i ==4) {
      for (i = 0; i <4; i++)
        char_array_4[i] = base64_chars.find(char_array_4[i]);

      char_array_3[0] = (char_array_4[0] << 2) + ((char_array_4[1] & 0x30) >> 4);
      char_array_3[1] = ((char_array_4[1] & 0xf) << 4) + ((char_array_4[2] & 0x3c) >> 2);
      char_array_3[2] = ((char_array_4[2] & 0x3) << 6) + char_array_4[3];

      for (i = 0; (i < 3); i++)
        ret += char_array_3[i];
      i = 0;
    }
  }

  if (i) {
    for (j = i; j <4; j++)
      char_array_4[j] = 0;

    for (j = 0; j <4; j++)
      char_array_4[j] = base64_chars.find(char_array_4[j]);

    char_array_3[0] = (char_array_4[0] << 2) + ((char_array_4[1] & 0x30) >> 4);
    char_array_3[1] = ((char_array_4[1] & 0xf) << 4) + ((char_array_4[2] & 0x3c) >> 2);
    char_array_3[2] = ((char_array_4[2] & 0x3) << 6) + char_array_4[3];

    for (j = 0; (j < i - 1); j++) ret += char_array_3[j];
  }

  return ret;
}

json::JSON signature_to_base64(sgx_ec256_signature_t* sig){
  json::JSON json_sig = json::JSON();
  json_sig["x"] = base64_encode((const unsigned char*) sig->x, SGX_ECP256_KEY_SIZE);
  json_sig["y"] = base64_encode((const unsigned char*) sig->y, SGX_ECP256_KEY_SIZE);
  return json_sig;
}

std::string ecc_sig_to_base64_string(sgx_ec256_signature_t* pk){
  return base64_encode((const unsigned char*) pk, 2*SGX_ECP256_KEY_SIZE);
}

json::JSON ecc_pk_to_base64(sgx_ec256_public_t* pk){
  json::JSON json_pk = json::JSON();
  json_pk["gx"] = base64_encode((const unsigned char*) pk->gx, SGX_ECP256_KEY_SIZE);
  json_pk["gy"] = base64_encode((const unsigned char*) pk->gy, SGX_ECP256_KEY_SIZE);
  return json_pk;
}

std::string ecc_pk_to_base64_string(sgx_ec256_public_t* pk){
  return base64_encode((const unsigned char*) pk, 2* SGX_ECP256_KEY_SIZE);
}

std::string sha256_to_base64(sgx_sha256_hash_t* hash){
  return base64_encode((const unsigned char*) hash, SGX_HASH_SIZE);
}

void ecc_pk_from_base64(json::JSON json_key, sgx_ec256_public_t *key){
  std::string gx = base64_decode(json_key["gx"].ToString());
  std::string gy = base64_decode(json_key["gy"].ToString());
  memcpy(&(key->gx), gx.c_str(), SGX_ECP256_KEY_SIZE);
  memcpy(&(key->gy), gy.c_str(), SGX_ECP256_KEY_SIZE);
  return;
}

std::string report_to_base64(sgx_quote_t* report){
  return base64_encode((const unsigned char*) report, sizeof(sgx_quote_t) + report->signature_len);
}

